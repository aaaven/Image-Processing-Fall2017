out{3,3}= r3'*c3'; 
subplot(4,4,11),imagesc((out{3,3}));
out{3,4}= r3'*c4'; 
subplot(4,4,12),imagesc((out{3,4}));
out{4,1}=-r4'*c1';  
subplot(4,4,13),imagesc((out{4,1}));
