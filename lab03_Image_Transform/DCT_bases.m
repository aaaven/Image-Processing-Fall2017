out{1,2} =-r1'*c2'; 
subplot(4,4,2), imagesc(out{1,2});
out{1,3} =-r1'*c3'; 
subplot(4,4,3), imagesc(out{1,3});
out{1,4} =-r1'*c4'; 
subplot(4,4,4), imagesc(out{1,4});
out{2,1} =-r2'*c1'; 
subplot(4,4,5), imagesc((out{2,1}));
out{2,2} = r2'*c2'; 
subplot(4,4,6), imagesc((out{2,2}));
out{2,3} = r2'*c3'; 
subplot(4,4,7), imagesc((out{2,3}));
out{2,4} = r2'*c4'; 
subplot(4,4,8), imagesc((out{2,4}));
out{3,1} =-r3'*c1'; 
